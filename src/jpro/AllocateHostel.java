    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String sid = id.getText();
        String sname = name.getText();
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/data", "root", "");
            s = con.prepareStatement("select * from student where id='" + sid + "'");
            r = s.executeQuery();

            if (r.next() && sname.equals(r.getString("sname"))) {
                if (!r.getString("residence").equals("-")) {
                    JOptionPane.showMessageDialog(null, "Residence Already allocated !!");
                } else {
                    hostel.setVisible(true);
                    room.setVisible(true);
                    cb1.setVisible(true);
                    cb2.setVisible(true);
                    addroom.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Information Incorrect !!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error!!");
        }

    }//GEN-LAST:event_loginActionPerformed

    private void cb1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb1ActionPerformed
        // TODO add your handling code here:
        if (cb1.getSelectedItem().equals("uwp")) {
            cost1.setVisible(true);
            cost.setText("Rs 3000/Month");
            cb2.removeAllItems();
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/data", "root", "");
                s = con.prepareStatement("select * from uwp where vacancy='yes'");
                r2 = s.executeQuery();
                while (r2.next()) {
                    if (r2.getString("vacancy").equals("yes")) {
                        cb2.addItem(r2.getString("room"));
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error!");
            }

        } else if (cb1.getSelectedItem().equals("rev")) {
            cost1.setVisible(true);
            cost.setText("Rs 5000/Month");
            cb2.removeAllItems();
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/data", "root", "");
                s = con.prepareStatement("select * from rev where vacancy='yes'");
                r2 = s.executeQuery();
                while (r2.next()) {
                    cb2.addItem(r2.getString("room"));
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error!");
            }
        } else if (cb1.getSelectedItem().equals("v1")) {
            cost1.setVisible(true);
            cost.setText("Rs 8000/Month");
            cb2.removeAllItems();
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/data", "root", "");
                s = con.prepareStatement("select * from v1 where vacancy='yes'");
                r2 = s.executeQuery();
                while (r2.next()) {
                    cb2.addItem(r2.getString("room"));
                }   
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error!");
            }

        }

    }//GEN-LAST:event_cb1ActionPerformed

