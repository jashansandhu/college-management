    public void addidtohosteltable() {
        String residence = cb1.getSelectedItem().toString();
        String room = cb2.getSelectedItem().toString();
        String sid = id.getText();
        String sname = name.getText();
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/data", "root", "");
            s = con.prepareStatement("update " + residence + " set student_id='" + sid + "', sname='" + sname + "', vacancy='no' where room='" + room + "'");
            s.executeUpdate();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error!");
        }
    }

